    SPEC_VALUE      VARCHAR(100)    NULL        COMMENT '규격값',
    LSL             DECIMAL(15,5)   NULL        COMMENT '하한규격',
    USL             DECIMAL(15,5)   NULL        COMMENT '상한규격',
    TARGET_VALUE    DECIMAL(15,5)   NULL        COMMENT '목표값',
