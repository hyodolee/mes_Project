-- 6.1 불량코드 (QC_DEFECT_CODE)
CREATE TABLE QC_DEFECT_CODE (
    DEFECT_CD       VARCHAR(20)     NOT NULL    COMMENT '불량코드',
    PLANT_CD        VARCHAR(20)     NOT NULL    COMMENT '공장코드',
    DEFECT_NM       VARCHAR(100)    NOT NULL    COMMENT '불량명',
    DEFECT_TYPE     VARCHAR(20)     NOT NULL    COMMENT '불량유형',
    DEFECT_GRP      VARCHAR(50)     NULL        COMMENT '불량그룹',
    DEFECT_DESC     VARCHAR(500)    NULL        COMMENT '불량설명',
    SEVERITY        VARCHAR(20)     NULL        COMMENT '심각도',
    SORT_SEQ        INT             NOT NULL    DEFAULT 0 COMMENT '정렬순서',
    USE_YN          CHAR(1)         NOT NULL    DEFAULT 'Y' COMMENT '사용여부',
    REG_USER_ID     VARCHAR(50)     NOT NULL    COMMENT '등록자ID',
    REG_DTM         DATETIME        NOT NULL    DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    UPD_USER_ID     VARCHAR(50)     NULL        COMMENT '수정자ID',
    UPD_DTM         DATETIME        NULL        ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
    PRIMARY KEY (DEFECT_CD),
    CONSTRAINT FK_QC_DEFECT_CODE_PLANT FOREIGN KEY (PLANT_CD) REFERENCES MST_PLANT(PLANT_CD)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='불량코드';

-- 6.2 검사기준 (QC_INSPECT_STD)
