    PLANT_CD        VARCHAR(20)     NOT NULL    COMMENT '공장코드',
    INSPECT_NO      VARCHAR(30)     NOT NULL    COMMENT '검사번호',
    INSPECT_TYPE    VARCHAR(20)     NOT NULL    COMMENT '검사유형',
    INSPECT_DT      DATE            NOT NULL    COMMENT '검사일자',
    ITEM_CD         VARCHAR(50)     NOT NULL    COMMENT '품목코드',
    LOT_NO          VARCHAR(50)     NULL        COMMENT 'LOT번호',
    INSPECT_QTY     DECIMAL(15,3)   NOT NULL    COMMENT '검사수량',
    SAMPLE_QTY      DECIMAL(15,3)   NULL        COMMENT '샘플수량',
    PASS_QTY        DECIMAL(15,3)   NULL        DEFAULT 0 COMMENT '합격수량',
    FAIL_QTY        DECIMAL(15,3)   NULL        DEFAULT 0 COMMENT '불합격수량',
    INSPECTOR_ID    VARCHAR(20)     NOT NULL    COMMENT '검사자ID',
    WO_ID           BIGINT          NULL        COMMENT '작업지시ID',
    RESULT_ID       BIGINT          NULL        COMMENT '작업실적ID',
    RECEIVE_ID      BIGINT          NULL        COMMENT '입고ID',
    VENDOR_CD       VARCHAR(50)     NULL        COMMENT '거래처코드',
    JUDGE_RESULT    VARCHAR(20)     NOT NULL    DEFAULT '대기' COMMENT '판정결과',
    JUDGE_USER_ID   VARCHAR(50)     NULL        COMMENT '판정자ID',
    JUDGE_DTM       DATETIME        NULL        COMMENT '판정일시',
    PROCESS_CD      VARCHAR(20)     NULL        COMMENT '공정코드',
    INSPECT_RMK     VARCHAR(500)    NULL        COMMENT '비고',
    REG_USER_ID     VARCHAR(50)     NOT NULL    COMMENT '등록자ID',
    REG_DTM         DATETIME        NOT NULL    DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    UPD_USER_ID     VARCHAR(50)     NULL        COMMENT '수정자ID',
    UPD_DTM         DATETIME        NULL        ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
    PRIMARY KEY (INSPECT_ID),
    UNIQUE KEY UK_QC_INSPECT_RESULT (INSPECT_NO),
    CONSTRAINT FK_QC_INSPECT_PLANT FOREIGN KEY (PLANT_CD) REFERENCES MST_PLANT(PLANT_CD),
    CONSTRAINT FK_QC_INSPECT_ITEM FOREIGN KEY (ITEM_CD) REFERENCES MST_ITEM(ITEM_CD),
    CONSTRAINT FK_QC_INSPECT_INSPECTOR FOREIGN KEY (INSPECTOR_ID) REFERENCES MST_WORKER(WORKER_ID),
    CONSTRAINT FK_QC_INSPECT_WO FOREIGN KEY (WO_ID) REFERENCES PLN_WORK_ORDER(WO_ID),
    CONSTRAINT FK_QC_INSPECT_VENDOR FOREIGN KEY (VENDOR_CD) REFERENCES MST_VENDOR(VENDOR_CD)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='검사실적';

