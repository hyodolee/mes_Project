    REG_USER_ID     VARCHAR(50)     NOT NULL    COMMENT '등록자ID',
    REG_DTM         DATETIME        NOT NULL    DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    UPD_USER_ID     VARCHAR(50)     NULL        COMMENT '수정자ID',
    UPD_DTM         DATETIME        NULL        ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
    PRIMARY KEY (INSPECT_STD_ID),
    UNIQUE KEY UK_QC_INSPECT_STD (PLANT_CD, ITEM_CD, INSPECT_TYPE, INSPECT_ITEM_SEQ),
    CONSTRAINT FK_QC_INSPECT_STD_PLANT FOREIGN KEY (PLANT_CD) REFERENCES MST_PLANT(PLANT_CD),
    CONSTRAINT FK_QC_INSPECT_STD_ITEM FOREIGN KEY (ITEM_CD) REFERENCES MST_ITEM(ITEM_CD)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='검사기준';

-- 6.3 검사실적 (QC_INSPECT_RESULT)
CREATE TABLE QC_INSPECT_RESULT (
