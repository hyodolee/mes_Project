    CONSTRAINT FK_EQP_DT_CODE_PLANT FOREIGN KEY (PLANT_CD) REFERENCES MST_PLANT(PLANT_CD)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='비가동코드';

-- 8.3 비가동이력 (EQP_DOWNTIME)
CREATE TABLE EQP_DOWNTIME (
