    DOWNTIME_REASON VARCHAR(500)    NULL        COMMENT '비가동사유',
    START_DTM       DATETIME        NOT NULL    COMMENT '시작일시',
    END_DTM         DATETIME        NULL        COMMENT '종료일시',
    DOWNTIME_MIN    DECIMAL(10,2)   NULL        COMMENT '비가동시간(분)',
    WO_ID           BIGINT          NULL        COMMENT '작업지시ID',
    OPER_ID         BIGINT          NULL        COMMENT '가동ID',
    ACTION_CONTENT  VARCHAR(500)    NULL        COMMENT '조치내용',
    ACTION_USER_ID  VARCHAR(20)     NULL        COMMENT '조치자ID',
    ACTION_DTM      DATETIME        NULL        COMMENT '조치일시',
    REPORTER_ID     VARCHAR(20)     NULL        COMMENT '보고자ID',
    DOWNTIME_RMK    VARCHAR(500)    NULL        COMMENT '비고',
    REG_USER_ID     VARCHAR(50)     NOT NULL    COMMENT '등록자ID',
    REG_DTM         DATETIME        NOT NULL    DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    UPD_USER_ID     VARCHAR(50)     NULL        COMMENT '수정자ID',
