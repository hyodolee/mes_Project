-- 8.1 설비가동현황 (EQP_OPER_STATUS)
CREATE TABLE EQP_OPER_STATUS (
    OPER_ID         BIGINT          NOT NULL    AUTO_INCREMENT COMMENT '가동ID',
    PLANT_CD        VARCHAR(20)     NOT NULL    COMMENT '공장코드',
    EQUIPMENT_CD    VARCHAR(20)     NOT NULL    COMMENT '설비코드',
    OPER_DT         DATE            NOT NULL    COMMENT '가동일자',
    SHIFT           VARCHAR(10)     NOT NULL    COMMENT '근무조',
    OPER_STATUS     VARCHAR(20)     NOT NULL    COMMENT '가동상태',
    START_DTM       DATETIME        NOT NULL    COMMENT '시작일시',
    END_DTM         DATETIME        NULL        COMMENT '종료일시',
    OPER_TIME       DECIMAL(10,2)   NULL        COMMENT '가동시간(분)',
    WO_ID           BIGINT          NULL        COMMENT '작업지시ID',
    ITEM_CD         VARCHAR(50)     NULL        COMMENT '품목코드',
    PROD_QTY        DECIMAL(15,3)   NULL        COMMENT '생산수량',
    WORKER_ID       VARCHAR(20)     NULL        COMMENT '작업자ID',
    OPER_RMK        VARCHAR(500)    NULL        COMMENT '비고',
    REG_USER_ID     VARCHAR(50)     NOT NULL    COMMENT '등록자ID',
    REG_DTM         DATETIME        NOT NULL    DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    UPD_USER_ID     VARCHAR(50)     NULL        COMMENT '수정자ID',
    UPD_DTM         DATETIME        NULL        ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
    PRIMARY KEY (OPER_ID),
    CONSTRAINT FK_EQP_OPER_PLANT FOREIGN KEY (PLANT_CD) REFERENCES MST_PLANT(PLANT_CD),
    CONSTRAINT FK_EQP_OPER_EQP FOREIGN KEY (EQUIPMENT_CD) REFERENCES MST_EQUIPMENT(EQUIPMENT_CD),
    CONSTRAINT FK_EQP_OPER_WO FOREIGN KEY (WO_ID) REFERENCES PLN_WORK_ORDER(WO_ID),
    CONSTRAINT FK_EQP_OPER_ITEM FOREIGN KEY (ITEM_CD) REFERENCES MST_ITEM(ITEM_CD),
    CONSTRAINT FK_EQP_OPER_WORKER FOREIGN KEY (WORKER_ID) REFERENCES MST_WORKER(WORKER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='설비가동현황';

-- 8.2 비가동코드 (EQP_DOWNTIME_CODE)
CREATE TABLE EQP_DOWNTIME_CODE (
    DOWNTIME_CD     VARCHAR(20)     NOT NULL    COMMENT '비가동코드',
