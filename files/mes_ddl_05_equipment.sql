    PRIMARY KEY (DOWNTIME_ID),
    CONSTRAINT FK_EQP_DOWNTIME_PLANT FOREIGN KEY (PLANT_CD) REFERENCES MST_PLANT(PLANT_CD),
    CONSTRAINT FK_EQP_DOWNTIME_EQP FOREIGN KEY (EQUIPMENT_CD) REFERENCES MST_EQUIPMENT(EQUIPMENT_CD),
    CONSTRAINT FK_EQP_DOWNTIME_CD FOREIGN KEY (DOWNTIME_CD) REFERENCES EQP_DOWNTIME_CODE(DOWNTIME_CD),
    CONSTRAINT FK_EQP_DOWNTIME_WO FOREIGN KEY (WO_ID) REFERENCES PLN_WORK_ORDER(WO_ID),
    CONSTRAINT FK_EQP_DOWNTIME_OPER FOREIGN KEY (OPER_ID) REFERENCES EQP_OPER_STATUS(OPER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='비가동이력';

-- 8.4 정비계획 (EQP_MAINT_PLAN)
CREATE TABLE EQP_MAINT_PLAN (
    MAINT_PLAN_ID   BIGINT          NOT NULL    AUTO_INCREMENT COMMENT '정비계획ID',
    PLANT_CD        VARCHAR(20)     NOT NULL    COMMENT '공장코드',
    EQUIPMENT_CD    VARCHAR(20)     NOT NULL    COMMENT '설비코드',
    MAINT_PLAN_NM   VARCHAR(100)    NOT NULL    COMMENT '정비계획명',
    MAINT_TYPE      VARCHAR(20)     NOT NULL    COMMENT '정비유형',
    CYCLE_TYPE      VARCHAR(20)     NOT NULL    COMMENT '주기유형',
    CYCLE_VALUE     INT             NOT NULL    COMMENT '주기값',
    CYCLE_UNIT      VARCHAR(20)     NOT NULL    DEFAULT '일' COMMENT '주기단위',
    BASE_DT         DATE            NULL        COMMENT '기준일',
    LAST_MAINT_DT   DATE            NULL        COMMENT '최종정비일',
    NEXT_MAINT_DT   DATE            NULL        COMMENT '다음정비예정일',
    LEAD_TIME       INT             NULL        DEFAULT 0 COMMENT '사전알림(일)',
    EST_MAINT_TIME  DECIMAL(10,2)   NULL        COMMENT '예상정비시간(분)',
    EST_COST        DECIMAL(18,2)   NULL        COMMENT '예상비용',
    MAINT_CHECKLIST TEXT            NULL        COMMENT '정비체크리스트',
    MAINT_CONTENT   VARCHAR(1000)   NULL        COMMENT '정비내용',
    MANAGER_ID      VARCHAR(20)     NULL        COMMENT '담당자ID',
    USE_YN          CHAR(1)         NOT NULL    DEFAULT 'Y' COMMENT '사용여부',
    REG_USER_ID     VARCHAR(50)     NOT NULL    COMMENT '등록자ID',
    REG_DTM         DATETIME        NOT NULL    DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    UPD_USER_ID     VARCHAR(50)     NULL        COMMENT '수정자ID',
    UPD_DTM         DATETIME        NULL        ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
    PRIMARY KEY (MAINT_PLAN_ID),
    CONSTRAINT FK_EQP_MAINT_PLAN_PLANT FOREIGN KEY (PLANT_CD) REFERENCES MST_PLANT(PLANT_CD),
    CONSTRAINT FK_EQP_MAINT_PLAN_EQP FOREIGN KEY (EQUIPMENT_CD) REFERENCES MST_EQUIPMENT(EQUIPMENT_CD)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='정비계획';

-- 8.5 정비이력 (EQP_MAINT_HIS)
CREATE TABLE EQP_MAINT_HIS (
    MAINT_ID        BIGINT          NOT NULL    AUTO_INCREMENT COMMENT '정비ID',
    PLANT_CD        VARCHAR(20)     NOT NULL    COMMENT '공장코드',
    EQUIPMENT_CD    VARCHAR(20)     NOT NULL    COMMENT '설비코드',
    MAINT_NO        VARCHAR(30)     NOT NULL    COMMENT '정비번호',
    MAINT_TYPE      VARCHAR(20)     NOT NULL    COMMENT '정비유형',
    MAINT_DT        DATE            NOT NULL    COMMENT '정비일자',
    MAINT_PLAN_ID   BIGINT          NULL        COMMENT '정비계획ID',
    DOWNTIME_ID     BIGINT          NULL        COMMENT '비가동ID',
    START_DTM       DATETIME        NOT NULL    COMMENT '시작일시',
    END_DTM         DATETIME        NULL        COMMENT '종료일시',
    MAINT_TIME      DECIMAL(10,2)   NULL        COMMENT '정비시간(분)',
    MAINT_WORKER_ID VARCHAR(20)     NULL        COMMENT '정비자ID',
    MAINT_TEAM      VARCHAR(100)    NULL        COMMENT '정비팀',
    MAINT_CONTENT   VARCHAR(1000)   NULL        COMMENT '정비내용',
    SYMPTOM         VARCHAR(500)    NULL        COMMENT '고장증상',
    CAUSE           VARCHAR(500)    NULL        COMMENT '고장원인',
    ACTION          VARCHAR(500)    NULL        COMMENT '조치내용',
    PART_REPLACED   VARCHAR(500)    NULL        COMMENT '교체부품',
    PART_COST       DECIMAL(18,2)   NULL        DEFAULT 0 COMMENT '부품비용',
    LABOR_COST      DECIMAL(18,2)   NULL        DEFAULT 0 COMMENT '인건비',
    OTHER_COST      DECIMAL(18,2)   NULL        DEFAULT 0 COMMENT '기타비용',
    MAINT_COST      DECIMAL(18,2)   GENERATED ALWAYS AS (COALESCE(PART_COST,0) + COALESCE(LABOR_COST,0) + COALESCE(OTHER_COST,0)) STORED COMMENT '총정비비용',
    MAINT_RESULT    VARCHAR(20)     NOT NULL    DEFAULT '진행' COMMENT '정비결과',
    NEXT_MAINT_DT   DATE            NULL        COMMENT '다음정비예정일',
    MAINT_RMK       VARCHAR(500)    NULL        COMMENT '비고',
    REG_USER_ID     VARCHAR(50)     NOT NULL    COMMENT '등록자ID',
    REG_DTM         DATETIME        NOT NULL    DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    UPD_USER_ID     VARCHAR(50)     NULL        COMMENT '수정자ID',
    UPD_DTM         DATETIME        NULL        ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
    PRIMARY KEY (MAINT_ID),
    UNIQUE KEY UK_EQP_MAINT_HIS (MAINT_NO),
    CONSTRAINT FK_EQP_MAINT_HIS_PLANT FOREIGN KEY (PLANT_CD) REFERENCES MST_PLANT(PLANT_CD),
    CONSTRAINT FK_EQP_MAINT_HIS_EQP FOREIGN KEY (EQUIPMENT_CD) REFERENCES MST_EQUIPMENT(EQUIPMENT_CD),
    CONSTRAINT FK_EQP_MAINT_HIS_PLAN FOREIGN KEY (MAINT_PLAN_ID) REFERENCES EQP_MAINT_PLAN(MAINT_PLAN_ID),
    CONSTRAINT FK_EQP_MAINT_HIS_DT FOREIGN KEY (DOWNTIME_ID) REFERENCES EQP_DOWNTIME(DOWNTIME_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='정비이력';

