-- ============================================================
-- MCS Route Optimization Extension
-- Target DB: MES_DB
-- Purpose  : Add route graph, route calculation result tables,
--            common codes, and demo route data for MCS optimization.
-- Run      : Execute after MCS_install.sql or against an existing MCS DB.
-- ============================================================

SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;

-- ============================================================
-- 1. Route master tables
-- ============================================================

CREATE TABLE IF NOT EXISTS MCS_ROUTE_NODE (
    ROUTE_NODE_ID  BIGINT        NOT NULL AUTO_INCREMENT COMMENT '경로노드ID',
    PLANT_CD       VARCHAR(20)   NOT NULL COMMENT '공장코드',
    NODE_CD        VARCHAR(50)   NOT NULL COMMENT '노드코드',
    NODE_NM        VARCHAR(100)  NOT NULL COMMENT '노드명',
    NODE_TYPE      VARCHAR(20)   NOT NULL COMMENT '노드유형 (MCS_NODE_TYPE)',
    LOCATION_ID    BIGINT        DEFAULT NULL COMMENT '로케이션ID',
    USE_YN         CHAR(1)       NOT NULL DEFAULT 'Y' COMMENT '사용여부',
    REG_USER_ID    VARCHAR(50)   NOT NULL COMMENT '등록자ID',
    REG_DTM        DATETIME      NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    UPD_USER_ID    VARCHAR(50)   DEFAULT NULL COMMENT '수정자ID',
    UPD_DTM        DATETIME      DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
    PRIMARY KEY (ROUTE_NODE_ID),
    UNIQUE KEY UK_MCS_ROUTE_NODE (PLANT_CD, NODE_CD),
    KEY IDX_MCS_ROUTE_NODE_01 (PLANT_CD, NODE_TYPE),
    KEY IDX_MCS_ROUTE_NODE_02 (LOCATION_ID),
    CONSTRAINT FK_MCS_ROUTE_NODE_PLANT FOREIGN KEY (PLANT_CD) REFERENCES MST_PLANT(PLANT_CD),
    CONSTRAINT FK_MCS_ROUTE_NODE_LOC FOREIGN KEY (LOCATION_ID) REFERENCES MCS_LOCATION(LOCATION_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='MCS 경로 노드';

CREATE TABLE IF NOT EXISTS MCS_ROUTE_EDGE (
    ROUTE_EDGE_ID   BIGINT        NOT NULL AUTO_INCREMENT COMMENT '경로구간ID',
    PLANT_CD        VARCHAR(20)   NOT NULL COMMENT '공장코드',
    EDGE_CD         VARCHAR(50)   NOT NULL COMMENT '구간코드',
    EDGE_NM         VARCHAR(100)  NOT NULL COMMENT '구간명',
    FROM_NODE_ID    BIGINT        NOT NULL COMMENT '출발노드ID',
    TO_NODE_ID      BIGINT        NOT NULL COMMENT '도착노드ID',
    BIDIRECTIONAL_YN CHAR(1)      NOT NULL DEFAULT 'Y' COMMENT '양방향여부',
    DISTANCE_M      DECIMAL(10,2) NOT NULL DEFAULT 0.00 COMMENT '거리(m)',
    TRAVEL_TIME_SEC INT           NOT NULL DEFAULT 0 COMMENT '예상시간(초)',
    BASE_COST       DECIMAL(10,2) NOT NULL DEFAULT 0.00 COMMENT '기본비용',
    EDGE_STATUS     VARCHAR(20)   NOT NULL DEFAULT 'AVAILABLE' COMMENT '구간상태 (MCS_EDGE_STATUS)',
    USE_YN          CHAR(1)       NOT NULL DEFAULT 'Y' COMMENT '사용여부',
    REG_USER_ID     VARCHAR(50)   NOT NULL COMMENT '등록자ID',
    REG_DTM         DATETIME      NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    UPD_USER_ID     VARCHAR(50)   DEFAULT NULL COMMENT '수정자ID',
    UPD_DTM         DATETIME      DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
    PRIMARY KEY (ROUTE_EDGE_ID),
    UNIQUE KEY UK_MCS_ROUTE_EDGE (PLANT_CD, EDGE_CD),
    KEY IDX_MCS_ROUTE_EDGE_01 (PLANT_CD, EDGE_STATUS),
    KEY IDX_MCS_ROUTE_EDGE_02 (FROM_NODE_ID),
    KEY IDX_MCS_ROUTE_EDGE_03 (TO_NODE_ID),
    CONSTRAINT FK_MCS_ROUTE_EDGE_PLANT FOREIGN KEY (PLANT_CD) REFERENCES MST_PLANT(PLANT_CD),
    CONSTRAINT FK_MCS_ROUTE_EDGE_FROM FOREIGN KEY (FROM_NODE_ID) REFERENCES MCS_ROUTE_NODE(ROUTE_NODE_ID),
    CONSTRAINT FK_MCS_ROUTE_EDGE_TO FOREIGN KEY (TO_NODE_ID) REFERENCES MCS_ROUTE_NODE(ROUTE_NODE_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='MCS 경로 구간';

CREATE TABLE IF NOT EXISTS MCS_TRANSFER_ROUTE (
    TRANSFER_ROUTE_ID BIGINT        NOT NULL AUTO_INCREMENT COMMENT '이동경로ID',
    TRANSFER_ID       BIGINT        NOT NULL COMMENT '이동ID',
    ROUTE_STATUS      VARCHAR(20)   NOT NULL DEFAULT 'PLANNED' COMMENT '경로상태 (MCS_ROUTE_STATUS)',
    TOTAL_DISTANCE_M  DECIMAL(10,2) NOT NULL DEFAULT 0.00 COMMENT '총거리(m)',
    TOTAL_TIME_SEC    INT           NOT NULL DEFAULT 0 COMMENT '총예상시간(초)',
    TOTAL_COST        DECIMAL(10,2) NOT NULL DEFAULT 0.00 COMMENT '총비용',
    OPTIMIZE_RULE     VARCHAR(30)   NOT NULL DEFAULT 'SHORTEST_TIME' COMMENT '최적화기준 (MCS_OPTIMIZE_RULE)',
    REPLAN_COUNT      INT           NOT NULL DEFAULT 0 COMMENT '재계산횟수',
    REG_USER_ID       VARCHAR(50)   NOT NULL COMMENT '등록자ID',
    REG_DTM           DATETIME      NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    UPD_USER_ID       VARCHAR(50)   DEFAULT NULL COMMENT '수정자ID',
    UPD_DTM           DATETIME      DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
    PRIMARY KEY (TRANSFER_ROUTE_ID),
    UNIQUE KEY UK_MCS_TRANSFER_ROUTE (TRANSFER_ID),
    KEY IDX_MCS_TRANSFER_ROUTE_01 (ROUTE_STATUS),
    CONSTRAINT FK_MCS_TRANSFER_ROUTE_ORDER FOREIGN KEY (TRANSFER_ID) REFERENCES MCS_TRANSFER_ORDER(TRANSFER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='MCS 이동 오더 선택 경로';

CREATE TABLE IF NOT EXISTS MCS_TRANSFER_ROUTE_STEP (
    ROUTE_STEP_ID     BIGINT      NOT NULL AUTO_INCREMENT COMMENT '이동경로단계ID',
    TRANSFER_ROUTE_ID BIGINT      NOT NULL COMMENT '이동경로ID',
    STEP_SEQ          INT         NOT NULL COMMENT '단계순번',
    ROUTE_EDGE_ID     BIGINT      NOT NULL COMMENT '경로구간ID',
    FROM_NODE_ID      BIGINT      NOT NULL COMMENT '출발노드ID',
    TO_NODE_ID        BIGINT      NOT NULL COMMENT '도착노드ID',
    STEP_STATUS       VARCHAR(20) NOT NULL DEFAULT 'WAITING' COMMENT '단계상태 (MCS_ROUTE_STEP_STATUS)',
    EXPECTED_TIME_SEC INT         NOT NULL DEFAULT 0 COMMENT '예상시간(초)',
    START_DTM         DATETIME    DEFAULT NULL COMMENT '시작일시',
    END_DTM           DATETIME    DEFAULT NULL COMMENT '종료일시',
    REG_USER_ID       VARCHAR(50) NOT NULL COMMENT '등록자ID',
    REG_DTM           DATETIME    NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    UPD_USER_ID       VARCHAR(50) DEFAULT NULL COMMENT '수정자ID',
    UPD_DTM           DATETIME    DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
    PRIMARY KEY (ROUTE_STEP_ID),
    UNIQUE KEY UK_MCS_TRANSFER_ROUTE_STEP (TRANSFER_ROUTE_ID, STEP_SEQ),
    KEY IDX_MCS_TRANSFER_ROUTE_STEP_01 (ROUTE_EDGE_ID),
    KEY IDX_MCS_TRANSFER_ROUTE_STEP_02 (STEP_STATUS),
    CONSTRAINT FK_MCS_ROUTE_STEP_ROUTE FOREIGN KEY (TRANSFER_ROUTE_ID) REFERENCES MCS_TRANSFER_ROUTE(TRANSFER_ROUTE_ID),
    CONSTRAINT FK_MCS_ROUTE_STEP_EDGE FOREIGN KEY (ROUTE_EDGE_ID) REFERENCES MCS_ROUTE_EDGE(ROUTE_EDGE_ID),
    CONSTRAINT FK_MCS_ROUTE_STEP_FROM FOREIGN KEY (FROM_NODE_ID) REFERENCES MCS_ROUTE_NODE(ROUTE_NODE_ID),
    CONSTRAINT FK_MCS_ROUTE_STEP_TO FOREIGN KEY (TO_NODE_ID) REFERENCES MCS_ROUTE_NODE(ROUTE_NODE_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='MCS 이동 오더 경로 단계';

CREATE TABLE IF NOT EXISTS MCS_ROUTE_EDGE_STATUS_HIS (
    STATUS_HIS_ID BIGINT       NOT NULL AUTO_INCREMENT COMMENT '구간상태이력ID',
    ROUTE_EDGE_ID BIGINT       NOT NULL COMMENT '경로구간ID',
    PREV_STATUS   VARCHAR(20)  DEFAULT NULL COMMENT '이전상태',
    NEW_STATUS    VARCHAR(20)  NOT NULL COMMENT '변경상태',
    REASON_TYPE   VARCHAR(20)  NOT NULL DEFAULT 'SYSTEM' COMMENT '변경사유유형',
    REASON_TEXT   VARCHAR(500) DEFAULT NULL COMMENT '변경사유',
    REF_EVENT_ID  BIGINT       DEFAULT NULL COMMENT 'PLC이벤트ID',
    REG_USER_ID   VARCHAR(50)  NOT NULL COMMENT '등록자ID',
    REG_DTM       DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    PRIMARY KEY (STATUS_HIS_ID),
    KEY IDX_MCS_ROUTE_EDGE_STATUS_01 (ROUTE_EDGE_ID, REG_DTM),
    KEY IDX_MCS_ROUTE_EDGE_STATUS_02 (REF_EVENT_ID),
    CONSTRAINT FK_MCS_ROUTE_EDGE_STATUS_EDGE FOREIGN KEY (ROUTE_EDGE_ID) REFERENCES MCS_ROUTE_EDGE(ROUTE_EDGE_ID),
    CONSTRAINT FK_MCS_ROUTE_EDGE_STATUS_EVENT FOREIGN KEY (REF_EVENT_ID) REFERENCES MCS_PLC_EVENT_LOG(EVENT_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='MCS 경로 구간 상태 이력';

