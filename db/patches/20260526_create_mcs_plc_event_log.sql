CREATE TABLE IF NOT EXISTS MCS_PLC_EVENT_LOG (
    EVENT_ID        BIGINT          NOT NULL AUTO_INCREMENT COMMENT 'PLC이벤트ID',
    EQUIPMENT_CD    VARCHAR(50)     NOT NULL COMMENT '설비코드',
    EVENT_TYPE      VARCHAR(50)     NOT NULL COMMENT '이벤트유형',
    EVENT_STATUS    VARCHAR(20)     NOT NULL DEFAULT 'NORMAL' COMMENT '이벤트상태',
    TARGET_TYPE     VARCHAR(30)     DEFAULT NULL COMMENT '대상유형',
    TARGET_ID       BIGINT          DEFAULT NULL COMMENT '대상ID',
    LOCATION_CD     VARCHAR(50)     DEFAULT NULL COMMENT '로케이션코드',
    ERROR_CODE      VARCHAR(50)     DEFAULT NULL COMMENT '오류코드',
    EVENT_MESSAGE   VARCHAR(500)    DEFAULT NULL COMMENT '이벤트메시지',
    RAW_PAYLOAD     LONGTEXT        DEFAULT NULL COMMENT '원본Payload',
    EVENT_DTM       DATETIME        NOT NULL COMMENT '이벤트발생일시',
    PROCESSED_YN    CHAR(1)         NOT NULL DEFAULT 'N' COMMENT '처리여부',
    PROCESS_RESULT  VARCHAR(20)     DEFAULT NULL COMMENT '처리결과',
    PROCESS_MESSAGE VARCHAR(500)    DEFAULT NULL COMMENT '처리메시지',
    PROCESSED_DTM   DATETIME        DEFAULT NULL COMMENT '처리일시',
    REG_USER_ID     VARCHAR(50)     NOT NULL COMMENT '등록자ID',
    REG_DTM         DATETIME        NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '등록일시',
    PRIMARY KEY (EVENT_ID),
    KEY IDX_MCS_PLC_EVENT_01 (EVENT_DTM),
    KEY IDX_MCS_PLC_EVENT_02 (TARGET_TYPE, TARGET_ID),
    KEY IDX_MCS_PLC_EVENT_03 (EVENT_TYPE, EVENT_STATUS),
    KEY IDX_MCS_PLC_EVENT_04 (PROCESSED_YN, PROCESS_RESULT)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='MCS PLC 이벤트 수신 로그';
